package com.nux.bb.near.u.track.util;
public class HURLEncoder {
	public HURLEncoder() {
	}
	public static String pathEncode(String text) {
	    StringBuffer StrUrl = new StringBuffer();
	    for (int i = 0; i < text.length(); ++i) {
	      switch (text.charAt(i)) {
	        case ' ':
	          StrUrl.append("_");
	          break;
	        case '+':
	          StrUrl.append("_");
	          break;
	        case '\'':
	          StrUrl.append("_");
	          break;
	        case '/':
	          StrUrl.append("_");
	          break;
	        case '<':
	          StrUrl.append("_");
	          break;
	        case '>':
	          StrUrl.append("_");
	          break;
	        case '#':
	          StrUrl.append("_");
	          break;
	        case '%':
	          StrUrl.append("_");
	          break;
	        case '&':
	          StrUrl.append("_");
	          break;
	        case '{':
	          StrUrl.append("_");
	          break;
	        case '}':
	          StrUrl.append("_");
	          break;
	        case '\\':
	          StrUrl.append("_");
	          break;
	        case '^':
	          StrUrl.append("_");
	          break;
	        case '~':
	          StrUrl.append("_");
	          break;
	        case '[':
	          StrUrl.append("_");
	          break;
	        case ']':
	          StrUrl.append("_");
	          break;
	        case '?':
		          StrUrl.append("_");
		          break;
	        default:
	          StrUrl.append(text.charAt(i));
	          break;
	      }
	    }
	    return StrUrl.toString();
	  }
} 